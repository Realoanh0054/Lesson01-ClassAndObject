package advance.dev;

public class Person {
	String name;
	int old;
	String adress;
	int phone;
	float average;
	
	public Person(String name, int old, String adress, int phone, float average) {
		super();
		this.name = name;
		this.old = old;
		this.adress = adress;
		this.phone = phone;
		this.average = average;
	}
	
}